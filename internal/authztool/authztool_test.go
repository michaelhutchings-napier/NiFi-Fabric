func newKeycloakTestServer(t *testing.T) *httptest.Server {
	t.Helper()

	return httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		switch {
		case r.Method == http.MethodGet && r.URL.Path == "/admin/realms/nifi/groups":
			w.Header().Set("Content-Type", "application/json")
			_, _ = w.Write([]byte(`[
  {"name":"ops","path":"/ops","subGroups":[{"name":"nifi-platform-admins","path":"/ops/nifi-platform-admins","subGroups":[]}]},
  {"name":"apps","path":"/apps","subGroups":[{"name":"nifi-platform-admins","path":"/apps/nifi-platform-admins","subGroups":[]}]}
]`))
		default:
			http.NotFound(w, r)
		}
	}))
}
