func TestValidateKeycloakMappingsDetectsDuplicatesInNameMode(t *testing.T) {
	t.Parallel()

	server := newKeycloakTestServer(t)
	cfg := Config{
		Source: SourceConfig{
			Type:           SourceTypeKeycloak,
			BaseURL:        server.URL,
			Realm:          "nifi",
			GroupsClaim:    "groups",
			GroupValueMode: GroupValueModeName,
		},
		Authz: AuthzConfig{
			InitialAdminGroup: "nifi-platform-admins",
		},
		Bindings: []Binding{
			{KeycloakGroup: "nifi-platform-admins", Bundles: []string{"admin"}},
		},
	}

	err := ValidateKeycloakMappings(context.Background(), cfg, Credentials{
		AccessToken: "test-token",
	})
	if err == nil {
		t.Fatalf("expected validation error")
	}
	if !strings.Contains(err.Error(), `duplicate Keycloak group value "nifi-platform-admins"`) {
		t.Fatalf("unexpected error: %v", err)
	}
}

