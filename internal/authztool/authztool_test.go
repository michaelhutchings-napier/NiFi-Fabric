func TestLoadConfigDefaultsGroupValueMode(t *testing.T) {
	t.Parallel()

	dir := t.TempDir()
	configPath := filepath.Join(dir, "config.yaml")
	if err := os.WriteFile(configPath, []byte(`
source:
  type: keycloak
  baseUrl: https://keycloak.example.com
  realm: nifi
  groupsClaim: groups

authz:
  initialAdminGroup: nifi-platform-admins

bindings:
- keycloakGroup: nifi-platform-admins
  bundles: [admin]
`), 0o644); err != nil {
		t.Fatalf("write config: %v", err)
	}

	cfg, err := LoadConfig(configPath)
	if err != nil {
		t.Fatalf("load config: %v", err)
	}

	if got, want := cfg.Source.GroupValueMode, GroupValueModeName; got != want {
		t.Fatalf("group value mode = %q, want %q", got, want)
	}
}

func TestRenderConfigAppSurface(t *testing.T) {
	t.Parallel()

	cfg := Config{
		Source: SourceConfig{
			Type:           SourceTypeKeycloak,
			BaseURL:        "https://keycloak.example.com",
			Realm:          "nifi",
			GroupsClaim:    "groups",
			GroupValueMode: GroupValueModeName,
		},
		Authz: AuthzConfig{
			InitialAdminGroup: "nifi-platform-admins",
		},
		Bindings: []Binding{
			{KeycloakGroup: "nifi-platform-admins", Bundles: []string{"admin"}},
			{KeycloakGroup: "nifi-flow-operators", Bundles: []string{"editor", "flowVersionManager", "viewer"}},
			{KeycloakGroup: "nifi-flow-observers", Bundles: []string{"viewer"}},
		},
	}

	rendered, err := RenderConfig(cfg, RenderOptions{
		Surface:       SurfaceApp,
		HeaderComment: true,
	})
	if err != nil {
		t.Fatalf("render config: %v", err)
	}

	want := `# Code generated by nifi-fabric-authz; DO NOT EDIT.
authz:
    mode: externalClaimGroups
    bootstrap:
        initialAdminGroup: nifi-platform-admins
    applicationGroups:
        - nifi-flow-observers
        - nifi-flow-operators
        - nifi-platform-admins
    bundles:
        viewer:
            groups:
                - nifi-flow-observers
                - nifi-flow-operators
        editor:
            groups:
                - nifi-flow-operators
        flowVersionManager:
            groups:
                - nifi-flow-operators
        admin:
            groups:
                - nifi-platform-admins
`

	if got := string(rendered); got != want {
		t.Fatalf("rendered output mismatch\nwant:\n%s\ngot:\n%s", want, got)
	}
}

